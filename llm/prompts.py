"""Шаблоны промптов для LLM-извлечения и анализа."""


def build_extraction_prompt(
    vocabulary: dict[str, str],
    rules: list[tuple[str, str]],
) -> str:
    """Строит системный промпт для извлечения, подставляя доменный словарь и правила."""
    vocab_lines = "\n".join(
        f"  - {name}: {desc}" for name, desc in vocabulary.items()
    )
    rules_lines = "\n".join(
        f"  - {label}: {formula}" for label, formula in rules
    )

    return f"""\
Ты — движок извлечения логических утверждений для фактчекинга резюме.

Получив текст резюме, извлеки фактические утверждения в виде формул пропозициональной логики.

Используй ТОЛЬКО эти связки:
  ~ (не), & (и), | (или), -> (влечёт), <-> (эквивалентность)

КРИТИЧЕСКИ ВАЖНО — используй ИМЕННО эти доменные переменные (не придумывай свои):
{vocab_lines}

Если утверждение из резюме соответствует одной из переменных выше — ОБЯЗАТЕЛЬНО используй её.
Можешь добавлять свои переменные ТОЛЬКО для фактов, которые не покрываются словарём.
Если утверждение НЕ покрывается ни одной переменной из словаря — ПРОПУСТИ его, не придумывай натянутую привязку.

═══════════════════════════════════════════════════════
ОПРЕДЕЛЕНИЕ fastChanges — ЧИТАЙ ВНИМАТЕЛЬНО
═══════════════════════════════════════════════════════

fastChanges = ИСКЛЮЧИТЕЛЬНО частота изменений ИСХОДНОГО КОДА и циклов РЕЛИЗА/ДЕПЛОЯ.
Конкретные маркеры: «сократил цикл релиза», «выпускали фичи каждый день», \
«перешли на еженедельные релизы», «увеличили частоту деплоев».

НЕ ЯВЛЯЕТСЯ fastChanges (ЗАПРЕЩЕНО классифицировать как fastChanges):
• Настройка очередей сообщений (RabbitMQ, Kafka) — это автоматизация бизнес-процессов
• Контейнеризация (Docker, K8s) — это инфраструктура деплоя
• Real-time обновления для пользователей (WebSocket, SignalR, NOTIFY) — это UX/данные
• Сокращение задержки ответа / латенси — это производительность системы
• Автоматизация процессов (фоновые задачи, обработка событий) — это автоматизация
• Retry-механизмы, обработка ошибок — это надёжность

ПРИМЕР ПРАВИЛЬНОЙ классификации:
  «сократил цикл запуска с 5 дней до 1 часа» → fastChanges ✓
  «настроил обработку событий из RabbitMQ» → НЕ fastChanges, ПРОПУСТИТЬ ✓
  «контейнеризировал сервис» → НЕ fastChanges, ПРОПУСТИТЬ ✓
  «внедрил real-time обновление через SignalR» → НЕ fastChanges, ПРОПУСТИТЬ ✓
  «внедрил near real-time обновления через PostgreSQL NOTIFY» → НЕ fastChanges, ПРОПУСТИТЬ ✓

═══════════════════════════════════════════════════════

Доменные правила (для справки — эти правила будут применены к твоим формулам):
{rules_lines}

Верни JSON-объект строго следующей структуры:
{{
  "claims": [
    {{
      "label": "claim_1",
      "original_text": "точная цитата из резюме",
      "formula": "fastChanges"
    }},
    ...
  ],
  "predicates_used": {{
    "fastChanges": "Кандидат обеспечивает быстрые изменения / короткие циклы разработки",
    ...
  }}
}}

Правила:
- Каждое утверждение должно соответствовать конкретному фактическому заявлению из резюме.
- Метки должны быть уникальными: claim_1, claim_2 и т.д.
- Извлеки от 5 до 15 утверждений в зависимости от длины резюме.
- Сосредоточься на утверждениях, которые потенциально могут противоречить друг другу \
или доменным правилам о: скорость vs качество, стабильность vs частые изменения, \
качество архитектуры vs упрощения.
- Убедись, что синтаксис формул — валидная пропозициональная логика.
- Описания предикатов в predicates_used пиши на русском языке.
- original_text — точная цитата из резюме на языке оригинала.

ФИНАЛЬНАЯ САМОПРОВЕРКА (выполни перед ответом):
Для каждого claim, где ты написал fastChanges, задай вопрос: \
«Говорит ли цитата БУКВАЛЬНО о частоте изменений кода или циклах релиза?» \
Если нет — УБЕРИ этот claim или замени переменную.
"""


